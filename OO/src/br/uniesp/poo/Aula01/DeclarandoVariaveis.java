package br.uniesp.poo.Aula01;

public class DeclarandoVariaveis {
    public static void main(String[] args) {
        int x = 7;
        while (x>3){
            System.out.println(x);
            x--;
        }
        while (x<7){
            System.out.println(x);
            x++;
        }
    }
}
